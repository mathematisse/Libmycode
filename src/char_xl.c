void xl_print(const char *str)
{
    const char *(*xl_char[])(int) = {
        xl_a, xl_b, xl_c, xl_d, xl_e, xl_f, xl_g, xl_h, xl_i, xl_j, xl_k, xl_l,
        xl_m, xl_n, xl_o, xl_p, xl_q, xl_r, xl_s, xl_t, xl_u, xl_v, xl_w, xl_x,
        xl_y, xl_z, xl_0, xl_1, xl_2, xl_3, xl_4, xl_5, xl_6, xl_7, xl_8, xl_9,
        xl_space, xl_equal, xl_less_than, xl_greater_than, xl_hash, xl_percent,
        xl_exclamation, xl_question, xl_dot, xl_comma, xl_colon, xl_semicolon,
        xl_quote, xl_dash, xl_slash, xl_backslash, xl_asterisk, xl_underscore,
        xl_bracket_open, xl_bracket_close, xl_brace_open, xl_brace_close,
        xl_paren_open, xl_paren_close, xl_plus};
    const char *chars = "abcdefghijklmnopqrstuvwxyz0123456789 =<>#%!?.,:;\"-/\\*_[]{}()+";

    for (int line = 0; line < 5; line++) {
        for (const char *p = str; *p; p++) {
            for (int i = 0; i < 63; i++) {
                if (*p == chars[i]) {
                    write(1, xl_char[i](line), 5);
                    break;
                }
            }
            write(1, " ", 1);
        }
        write(1, "\n", 1);
    }
}